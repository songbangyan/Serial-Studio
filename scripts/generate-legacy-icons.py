#!/usr/bin/env python3
"""Generate the legacy icon-path map from the spec-0028 migration manifest.

User project files persist workspace icons as qrc URLs; files written before the
icon-tree migration reference pre-migration paths. This script converts
doc/claude/specs/0028-icon-registry/icon-map.csv into
app/src/Misc/IconRegistryLegacy.cpp, a lookup Misc::legacyIconPath() consults so
those saved URLs keep resolving after the qrc compat aliases were dropped (task
T21). Deterministic; rerun only if the manifest ever changes.

Usage:
    python3 scripts/generate-legacy-icons.py
"""

